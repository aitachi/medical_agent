def main():
    """主函数：生成合并训练数据"""
    # 读取现有数据
    existing_data_path = "C:/Users/ASUS/Desktop/medical/medical_agent/tests/algorithem/test_dataset_5000.json"
    with open(existing_data_path, 'r', encoding='utf-8') as f:
        existing_data = json.load(f)

    existing_samples = existing_data.get('samples', [])
    print(f"现有样本数: {len(existing_samples)}")

    # 生成新样本
    new_samples = []

    # 新增意图样本
    followup_samples = generate_followup_samples()
    records_samples = generate_records_samples()
    my_appointment_samples = generate_my_appointment_samples()

    new_samples.extend(followup_samples)
    new_samples.extend(records_samples)
    new_samples.extend(my_appointment_samples)

    # 增强现有意图样本
    enhanced_samples = generate_enhanced_existing_samples()
    new_samples.extend(enhanced_samples)

    print(f"新增followup样本数: {len(followup_samples)}")
    print(f"新增records样本数: {len(records_samples)}")
    print(f"新增my_appointment样本数: {len(my_appointment_samples)}")
    print(f"增强现有意图样本数: {len(enhanced_samples)}")
    print(f"总新增样本数: {len(new_samples)}")

    # 合并数据
    all_samples = existing_samples + new_samples

    # 统计各意图数量
    intent_counts = {}
    for sample in all_samples:
        intent = sample['intent']
        intent_counts[intent] = intent_counts.get(intent, 0) + 1

    # 获取所有场景
    scenarios = set()
    for sample in all_samples:
        scenario = sample.get('scenario', 'unknown')
        scenarios.add(scenario)

    # 构建新数据
    merged_data = {
        "metadata": {
            "total_samples": len(all_samples),
            "generated_at": datetime.now().isoformat(),
            "intents": list(intent_counts.keys()),
            "scenarios": list(scenarios),
            "intent_counts": intent_counts,
            "original_samples": len(existing_samples),
            "new_samples": len(new_samples)
        },
        "samples": all_samples
    }

    # 保存合并数据
    output_path = "C:/Users/ASUS/Desktop/medical/medical_agent/tests/algorithem/extended_training_data.json"
    with open(output_path, 'w', encoding='utf-8') as f:
        json.dump(merged_data, f, ensure_ascii=False, indent=2)

    print(f"\n合并完成！")
    print(f"总样本数: {len(all_samples)}")
    print(f"保存路径: {output_path}")
    print(f"\n各意图样本数:")
    for intent, count in sorted(intent_counts.items()):
        print(f"  {intent}: {count}")

    return merged_data


if __name__ == "__main__":
    main()
